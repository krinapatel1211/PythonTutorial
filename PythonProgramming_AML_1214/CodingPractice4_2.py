places_to_visit = ["Switzerland", "Hawaii", "Germany", "Maldives", "Greece"]

print("\nOriginal List: ", places_to_visit)
print("Sorted list: ", sorted(places_to_visit))

print("\nOriginal List: ", places_to_visit)
print("Sorted list (descending): ", sorted(places_to_visit, reverse=True))
print("\nOriginal List: ", places_to_visit)
places_to_visit.reverse()
print("Reversed List: ", places_to_visit)
places_to_visit.reverse()
print("Reverse Again (Original List): ", places_to_visit)

places_to_visit.sort()
print("Sort List: ", places_to_visit)

places_to_visit.sort(reverse=True)
print("Reversely sort list: ", places_to_visit)
